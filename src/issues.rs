#[cfg(test)]
mod tests {
    use super::*;
    use crate::governance::{EvidenceClass, FeatureArea};

    #[test]
    fn issue_from_rca_has_title_and_body() {
        let steps = vec![
            ("Lock tree".to_owned(), true),
            ("Wait events".to_owned(), false),
        ];
        let issue = issue_from_rca("prod", "Lock cascade detected", &steps);
        assert!(issue.title.contains("RCA"));
        assert!(issue.title.contains("Lock cascade"));
        assert!(issue.body.contains("prod"));
        assert!(issue.body.contains("data collected"));
        assert!(issue.body.contains("no data"));
        assert!(issue.labels.contains(&"rca".to_owned()));
    }

    #[test]
    fn issue_from_proposals_none_when_empty() {
        assert!(issue_from_proposals("db", &[]).is_none());
    }

    #[test]
    fn issue_from_proposals_formats_findings() {
        let proposals = vec![
            ActionProposal {
                feature: FeatureArea::IndexHealth,
                severity: Severity::Warning,
                evidence_class: EvidenceClass::Heuristic,
                finding: "idx_foo is unused".to_owned(),
                proposed_action: "DROP INDEX CONCURRENTLY idx_foo".to_owned(),
                expected_outcome: "Free 100MB".to_owned(),
                risk: "Low".to_owned(),
                created_at: std::time::SystemTime::now(),
            },
            ActionProposal {
                feature: FeatureArea::IndexHealth,
                severity: Severity::Critical,
                evidence_class: EvidenceClass::Factual,
                finding: "idx_bar is invalid".to_owned(),
                proposed_action: "REINDEX CONCURRENTLY idx_bar".to_owned(),
                expected_outcome: "Restore index".to_owned(),
                risk: "Low".to_owned(),
                created_at: std::time::SystemTime::now(),
            },
        ];
        let issue = issue_from_proposals("prod", &proposals).unwrap();
        assert!(issue.title.contains("2 finding(s)"));
        assert!(issue.body.contains("idx_foo"));
        assert!(issue.body.contains("idx_bar"));
        // Max severity is Critical.
        assert!(issue.labels.contains(&"critical".to_owned()));
    }

    #[test]
    fn issue_from_anomaly_formats_correctly() {
        let issue = issue_from_anomaly("staging", "SessionSpike", "Active sessions jumped to 150");
        assert!(issue.title.contains("SessionSpike"));
        assert!(issue.body.contains("staging"));
        assert!(issue.body.contains("150"));
        assert!(issue.labels.contains(&"anomaly".to_owned()));
    }

    #[test]
    fn github_issue_creator_stores_repo() {
        let creator = GitHubIssueCreator::new("owner/repo".to_owned());
        assert_eq!(creator.repo, "owner/repo");
    }

    #[test]
    fn issue_template_labels_are_not_empty() {
        let issue = issue_from_rca("db", "test", &[]);
        assert!(!issue.labels.is_empty());
        assert!(issue.labels.contains(&"rpg".to_owned()));
    }

    #[test]
    fn issue_from_proposals_single_info_severity() {
        let proposals = vec![ActionProposal {
            feature: FeatureArea::ConfigTuning,
            severity: Severity::Info,
            evidence_class: EvidenceClass::Heuristic,
            finding: "timeout disabled".to_owned(),
            proposed_action: "ALTER SYSTEM SET statement_timeout = '30s'".to_owned(),
            expected_outcome: "Queries capped at 30s".to_owned(),
            risk: "Moderate".to_owned(),
            created_at: std::time::SystemTime::now(),
        }];
        let issue = issue_from_proposals("dev", &proposals).unwrap();
        assert!(issue.labels.contains(&"info".to_owned()));
        assert!(issue.title.contains("1 finding(s)"));
    }
}
