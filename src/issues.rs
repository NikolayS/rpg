/// Build an issue template from an RCA snapshot.
pub fn issue_from_rca(
    dbname: &str,
    anomaly_summary: &str,
    diagnostic_steps: &[(String, bool)],
) -> IssueTemplate {
    let mut body = String::new();
    writeln!(body, "## RCA Report — {dbname}").unwrap();
    writeln!(body).unwrap();
    writeln!(body, "**Anomaly:** {anomaly_summary}").unwrap();
    writeln!(body).unwrap();
    writeln!(body, "### Diagnostic Steps").unwrap();
    writeln!(body).unwrap();
    for (step_name, has_data) in diagnostic_steps {
        let icon = if *has_data {
            "data collected"
        } else {
            "no data"
        };
        writeln!(body, "- **{step_name}**: {icon}").unwrap();
    }
    writeln!(body).unwrap();
    writeln!(body, "---").unwrap();
    writeln!(body, "*Auto-generated by Rpg*").unwrap();

    IssueTemplate {
        title: format!("[Rpg] RCA: {anomaly_summary}"),
        body,
        labels: vec![
            "rpg".to_owned(),
            "rca".to_owned(),
            "auto-generated".to_owned(),
        ],
        source: "rca".to_owned(),
    }
}

/// Build an issue template from action proposals.
pub fn issue_from_proposals(dbname: &str, proposals: &[ActionProposal]) -> Option<IssueTemplate> {
    if proposals.is_empty() {
        return None;
    }

    let max_severity = proposals
        .iter()
        .map(|p| p.severity)
        .max()
        .unwrap_or(Severity::Info);

    let severity_label = match max_severity {
        Severity::Critical => "critical",
        Severity::Warning => "warning",
        Severity::Info => "info",
    };

    let feature = proposals[0].feature.label();

    let mut body = String::new();
    writeln!(body, "## Findings — {dbname}").unwrap();
    writeln!(body).unwrap();
    writeln!(
        body,
        "Rpg detected **{count}** actionable finding(s) in the `{feature}` area.",
        count = proposals.len(),
    )
    .unwrap();
    writeln!(body).unwrap();

    for (i, p) in proposals.iter().enumerate() {
        let sev = match p.severity {
            Severity::Critical => "!!",
            Severity::Warning => "!",
            Severity::Info => " ",
        };
        writeln!(body, "### {sev} Finding {}", i + 1).unwrap();
        writeln!(body).unwrap();
        writeln!(body, "**Problem:** {}", p.finding).unwrap();
        writeln!(body, "**Proposed action:** `{}`", p.proposed_action).unwrap();
        writeln!(body, "**Expected outcome:** {}", p.expected_outcome).unwrap();
        writeln!(body, "**Risk:** {}", p.risk).unwrap();
        writeln!(body).unwrap();
    }

    writeln!(body, "---").unwrap();
    writeln!(body, "*Auto-generated by Rpg*").unwrap();

    Some(IssueTemplate {
        title: format!(
            "[Rpg] {feature}: {count} finding(s) on {dbname}",
            count = proposals.len()
        ),
        body,
        labels: vec![
            "rpg".to_owned(),
            feature.to_owned(),
            severity_label.to_owned(),
            "auto-generated".to_owned(),
        ],
        source: feature.to_owned(),
    })
}

/// Build an issue template from an anomaly detection.
pub fn issue_from_anomaly(dbname: &str, anomaly_kind: &str, description: &str) -> IssueTemplate {
    let mut body = String::new();
    writeln!(body, "## Anomaly Detected — {dbname}").unwrap();
    writeln!(body).unwrap();
    writeln!(body, "**Type:** {anomaly_kind}").unwrap();
    writeln!(body, "**Description:** {description}").unwrap();
    writeln!(body).unwrap();
    writeln!(body, "---").unwrap();
    writeln!(body, "*Auto-generated by Rpg daemon*").unwrap();

    IssueTemplate {
        title: format!("[Rpg] Anomaly: {anomaly_kind} on {dbname}"),
        body,
        labels: vec![
            "rpg".to_owned(),
            "anomaly".to_owned(),
            "auto-generated".to_owned(),
        ],
        source: "anomaly".to_owned(),
    }
}

// ---------------------------------------------------------------------------
// Unit tests
