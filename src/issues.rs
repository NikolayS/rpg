//! Issue integration — create/update issues from agent findings.
//!
//! Supports creating issues in external trackers from RCA findings,
//! index health reports, and anomaly detections.  Currently supports:
//!
//! - **GitHub Issues**: via `gh` CLI or GitHub API
//! - **`PostgresAI` Issues**: via MCP tool bridge (runtime-provided)
//!
//! Template-based issue content ensures consistent formatting.

// Phase 3 infrastructure — consumers arrive in subsequent PRs.
#![allow(dead_code)]

use std::fmt::Write as _;

use crate::governance::{ActionProposal, Severity};

// ---------------------------------------------------------------------------
// Issue template
// ---------------------------------------------------------------------------

/// A structured issue ready to be created in an external tracker.
#[derive(Debug, Clone)]
pub struct IssueTemplate {
    /// Issue title (short summary).
    pub title: String,
    /// Issue body (Markdown).
    pub body: String,
    /// Labels to apply.
    pub labels: Vec<String>,
    /// Source feature area.
    pub source: String,
}

// ---------------------------------------------------------------------------
// GitHub Issues
// ---------------------------------------------------------------------------

/// GitHub Issues integration via the `gh` CLI.
#[derive(Debug)]
pub struct GitHubIssueCreator {
    /// Repository in `owner/repo` format.
    repo: String,
}

impl GitHubIssueCreator {
    /// Create a new GitHub issue creator for the given repository.
    pub fn new(repo: String) -> Self {
        Self { repo }
    }

    /// Create a GitHub issue using the `gh` CLI.
    ///
    /// Returns the issue URL on success, or an error message.
    pub async fn create_issue(&self, template: &IssueTemplate) -> Result<String, String> {
        let repo = self.repo.clone();
        let title = template.title.clone();
        let body = template.body.clone();
        let labels = template.labels.clone();

        tokio::task::spawn_blocking(move || {
            let mut cmd = std::process::Command::new("gh");
            cmd.args(["issue", "create", "--repo", &repo]);
            cmd.args(["--title", &title]);
            cmd.args(["--body", &body]);
            for label in &labels {
                cmd.args(["--label", label]);
            }

            let output = cmd
                .output()
                .map_err(|e| format!("Failed to run `gh` CLI (is it installed?): {e}"))?;

            if output.status.success() {
                let url = String::from_utf8_lossy(&output.stdout).trim().to_owned();
                Ok(url)
            } else {
                let stderr = String::from_utf8_lossy(&output.stderr).trim().to_owned();
                Err(format!("gh issue create failed: {stderr}"))
            }
        })
        .await
        .map_err(|e| format!("Task join error: {e}"))?
    }
}

