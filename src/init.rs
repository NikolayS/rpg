/// Convert a `pg_size_pretty` string to binary-unit notation.
///
/// `pg_size_pretty` returns strings like `"42 MB"`, `"1536 kB"`, `"2048 bytes"`.
/// We convert to MiB / KiB / GiB / TiB per the project style guide.
/// Values already in bytes are shown as-is.
fn convert_pg_size_to_binary(pg_size: &str) -> String {
    let parts: Vec<&str> = pg_size.trim().splitn(2, ' ').collect();
    if parts.len() != 2 {
        return pg_size.to_owned();
    }
    let num_str = parts[0];
    let unit = parts[1];

    let Ok(num) = num_str.parse::<f64>() else {
        return pg_size.to_owned();
    };

    // Convert to bytes first (Postgres uses 1024-based units internally).
    let bytes: f64 = match unit {
        "bytes" => num,
        "kB" => num * 1024.0,
        "MB" => num * 1024.0 * 1024.0,
        "GB" => num * 1024.0 * 1024.0 * 1024.0,
        "TB" => num * 1024.0 * 1024.0 * 1024.0 * 1024.0,
        _ => return pg_size.to_owned(),
    };

    if bytes < 1024.0 {
        format!("{bytes:.0} bytes")
    } else if bytes < 1024.0_f64.powi(2) {
        format!("{:.0} KiB", bytes / 1024.0)
    } else if bytes < 1024.0_f64.powi(3) {
        format!("{:.1} MiB", bytes / 1024.0_f64.powi(2))
    } else if bytes < 1024.0_f64.powi(4) {
        format!("{:.1} GiB", bytes / 1024.0_f64.powi(3))
    } else {
        format!("{:.1} TiB", bytes / 1024.0_f64.powi(4))
    }
}

/// Format a row-count string with thousands separators.
fn format_row_count(s: &str) -> String {
    let Ok(n) = s.parse::<u64>() else {
        return s.to_owned();
    };
    // Insert commas every 3 digits from the right.
    let raw = n.to_string();
    let mut out = String::with_capacity(raw.len() + raw.len() / 3);
    for (i, ch) in raw.chars().rev().enumerate() {
        if i > 0 && i % 3 == 0 {
            out.push(',');
        }
        out.push(ch);
    }
    out.chars().rev().collect()
}

