#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_convert_pg_size_bytes() {
        assert_eq!(convert_pg_size_to_binary("512 bytes"), "512 bytes");
    }

    #[test]
    fn test_convert_pg_size_kb() {
        assert_eq!(convert_pg_size_to_binary("512 kB"), "512 KiB");
    }

    #[test]
    fn test_convert_pg_size_mb() {
        assert_eq!(convert_pg_size_to_binary("42 MB"), "42.0 MiB");
    }

    #[test]
    fn test_convert_pg_size_gb() {
        assert_eq!(convert_pg_size_to_binary("2 GB"), "2.0 GiB");
    }

    #[test]
    fn test_format_row_count_small() {
        assert_eq!(format_row_count("42"), "42");
    }

    #[test]
    fn test_format_row_count_thousands() {
        assert_eq!(format_row_count("1234"), "1,234");
    }

    #[test]
    fn test_format_row_count_millions() {
        assert_eq!(format_row_count("1234567"), "1,234,567");
    }
}
